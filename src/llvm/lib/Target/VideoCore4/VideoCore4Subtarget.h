#ifndef LLVM_TARGET_VIDEOCORE4_SUBTARGET_H
#define LLVM_TARGET_VIDEOCORE4_SUBTARGET_H

#include "VideoCore4InstrInfo.h"
#include "VideoCore4ISelLowering.h"
#include "VideoCore4FrameLowering.h"
#include "VideoCore4SelectionDAGInfo.h"
#include "VideoCore4RegisterInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/MC/MCInstrItineraries.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "VideoCore4GenSubtargetInfo.inc"

namespace llvm {
class StringRef;
class VideoCore4InstrInfo;
struct VideoCore4RegisterInfo;
class VideoCore4TargetMachine;
class VideoCore4FrameLowering;
class VideoCore4TargetLowering;  
  
class VideoCore4Subtarget : public VideoCore4GenSubtargetInfo {
protected:
  enum VideoCoreArchEnum {
    videocore4I
  };
  VideoCoreArchEnum                               VideoCoreArchVersion;
  InstrItineraryData                              InstrItins;
  const VideoCore4TargetMachine                  &TM;
  //Triple                                         TargetTriple;
  const SelectionDAGTargetInfo                    TSInfo;
  std::unique_ptr<const VideoCore4InstrInfo>      InstrInfo;
  std::unique_ptr<const VideoCore4FrameLowering>  FrameLowering;
  std::unique_ptr<const VideoCore4TargetLowering> TLInfo;
  
public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ///
  VideoCore4Subtarget(const Triple                  &TT,
		      StringRef                      CPU,
		      StringRef                      FS,
		      const VideoCore4TargetMachine &TM);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU,
			      StringRef TuneCPU,
			      StringRef FS);

  unsigned stackAlignment() const { return 4; }                                                                             
  bool     useAA() const override { return true; }

  VideoCore4Subtarget&
  initializeSubtargetDependencies(StringRef            CPU,
                                  StringRef            FS,
                                  const TargetMachine &TM);

  const SelectionDAGTargetInfo*
  getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  const VideoCore4InstrInfo*
  getInstrInfo() const override {
    return InstrInfo.get();
  }
  
  const TargetFrameLowering*
  getFrameLowering() const override {
    return FrameLowering.get();
  }

  const VideoCore4RegisterInfo* getRegisterInfo() const override {
    return &InstrInfo->getRegisterInfo();
  }

  const VideoCore4TargetLowering*
  getTargetLowering() const override {
    return TLInfo.get();
  }

  const InstrItineraryData*
  getInstrItineraryData() const override {
    return &InstrItins;
  }

  CodeGenOpt::Level
  getOptLevelToEnablePostRAScheduler() const override {
    return CodeGenOpt::Aggressive;
    //return CodeGenOpt::Default;
  }

  bool
  enableMachineScheduler() const override {
    return true;
  }
};
} // End llvm namespace

#endif  // LLVM_TARGET_VideoCore4_SUBTARGET_H
